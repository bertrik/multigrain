void audioOn() {
  // Set up PWM to 31.25kHz, phase accurate
  TCCR2A = _BV(COM2A1) | _BV(WGM20);
  TCCR2B = _BV(CS20);
  TIMSK2 = _BV(TOIE2);
}


uint16_t sampleAveraged(uint8_t input, avg_t *avg) {
  // Sample it:
  uint16_t value = analogRead(input);

  // Compute moving average:
  avg->sum -= avg->window[avg->i];
  avg->sum += value;
  
  avg->window[avg->i] = value;
  avg->i++;
  avg->i %= SAMPLE_AVG_LEN;

  return avg->sum / SAMPLE_AVG_LEN;
}

uint8_t getClock() {
  static avg_t cv_avg;
  return sampleAveraged(CV_IN, &cv_avg) > 32;
}

uint8_t getTrigger(edge_t edge) {
  static uint8_t last_clock_state = 0;
  uint8_t clock_state, trigger;

  // Get triggers and rate values
  clock_state = getClock();

  trigger = edge == EDGE_RISING ? clock_state > last_clock_state : last_clock_state > clock_state;
  last_clock_state = clock_state;
  return trigger;
}

